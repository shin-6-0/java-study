package prob01;

public class Printer {
	private String text;
	
	public Printer() {}
	public Printer(String text) {
		super();
		this.text = text;
	}
	public void println(String text) {
		System.out.println(text);
	}
	public void println(int text) {
		System.out.println(text);
	}
	public void println(double text) {
		System.out.println(text);
	}
	public void println(boolean text) {
		System.out.println(text);
	}
}
